    private void boton_regresarModificarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_regresarModificarClienteActionPerformed
        // Se le va a solicitar al servidor cerrar la ventana abierta
        Peticion peticionIngresar = new Peticion(AccionServidora.CERRAR_VENTANA, "" );
        
        Conexion conexion = new Conexion(peticionIngresar);

        boolean respuesta = (boolean) conexion.getRespuestaServer();
        
        if (respuesta){
            this.setVisible(false);
            this.dispose();
        }
    }//GEN-LAST:event_boton_regresarModificarClienteActionPerformed

