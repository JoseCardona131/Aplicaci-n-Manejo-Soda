    private void boton_verCatalogoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_verCatalogoActionPerformed
        // Se le va a solicitar al servidor abrir una ventana nueva
        Peticion peticionIngresar = new Peticion(AccionServidora.VER_CATALOGO, "" );
        
        Conexion conexion = new Conexion(peticionIngresar);

        boolean respuesta = (boolean) conexion.getRespuestaServer();
        
        if (respuesta){
            JFrame parentFrame =  (JFrame) this.getParent().getParent();
            VerCatalogo nueva = new VerCatalogo(parentFrame, true);
            nueva.setVisible(true);
        }
    }//GEN-LAST:event_boton_verCatalogoActionPerformed

