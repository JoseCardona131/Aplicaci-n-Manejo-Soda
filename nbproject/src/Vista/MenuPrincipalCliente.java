    private void label_nextMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_label_nextMousePressed
        new Thread();
        try {
            Thread.sleep(300);
        } catch (InterruptedException ex) {
            Logger.getLogger(MenuPrincipalCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
        int p = this.label_imagenes.getX();
        
        if(p> -1){
            Animacion.Animacion.mover_izquierda(900,200,1,2,label_imagenes);
         
        }
        position = position+1;
        if(position>=takeimage().length){
            position = takeimage().length-1;
            
        }
        show(position);
    }//GEN-LAST:event_label_nextMousePressed

    private void label_backMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_label_backMousePressed
        new Thread();
        try {
            Thread.sleep(300);
        } catch (InterruptedException ex) {
            Logger.getLogger(MenuPrincipalCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
        int p = this.label_imagenes.getX();
        
        if(p> -1){
            Animacion.Animacion.mover_izquierda(900,200,1,2,label_imagenes);
         
        }
        position = position-1;
        if(position<0){
            position = 0;
            
        }
        show(position);
    }//GEN-LAST:event_label_backMousePressed

    private void boton_logoutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_logoutActionPerformed
        // Se le va a solicitar al servidor cerrar la ventana abierta
        Peticion peticionIngresar = new Peticion(AccionServidora.CERRAR_VENTANA, "" );
        
        Conexion conexion = new Conexion(peticionIngresar);

        boolean respuesta = (boolean) conexion.getRespuestaServer();
        
        if (respuesta){
            this.setVisible(false);
            this.dispose();
        }
    }//GEN-LAST:event_boton_logoutActionPerformed

